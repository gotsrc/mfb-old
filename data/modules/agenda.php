<h3>Family</h3>
<ol>
<li>Countries – <i>Families are located in different countries</i></li>
<li>Health Status – <i>Families have health status’ (If the family Don dies all other members need to be wiped out before the Family can be fully eradicated)</i></li>
<li>Respect – <i>Families have a respect status (So if the family is respected it will show the percentage how much they are respected and so on.)</i></li>
<li>Family Don can give him/herself money from the Family bank</li>
<li>Choice of Don – <i>Current Family don can choose who the next Don will be when he/she dies</i></li>
<li>Family Forum Special Access – <i>Special access control panel so that mob members can moderate the Family Forum (Don gets to choose who moderates)</i></li>
<li>Family Bank Pin Numbers – <i>Families have special pin numbers for money withdrawals, only the members YOU choose get this pin number.</i></li>
<li>Bullet Bank – <i>Family members can drop their bullets into the bullet bank, this is not displayed on Family home page</i></li>
<li>Status’ – <i>All families will have a Status such as Active, Suspended or Banned (This is for the Global Family Staff who look after the families and get the final say on which family is breaking the rules etc.)</i></li>
</ol>

<h3>Staff / Family Staff</h3>

<ol>
<li>Roles – <i>All of the staff get a certain role assigned to them
(E.g. Family Managers get to look after all Families within the game, Human
Resources manages all the players and staff, Accountants manage all the game
money.)</i></li>
<li>Global Money Management – <i>GMM will be for managing all in game money, for example
if the global money is set at £50b and is going down the assigned staff member
can update the total amount of in-game money</i></li>

<li>Staff Management – <i>List of roles available once created.</i></li>

<li>Property Staff – <i>Property
staff will handle all property queries, switchovers etc.</i></li>

<li>Family Staff – <i>These
people over see every single mob, and have the power to suspend/ban/drop a mob
if they feel that they are breaking the in-game rules.</i></li>

<li>Rule Enforcers – <i>Exactly
what the title says, this staff role will be for the main game moderators who
moderate the game and decide if someone is cheating or playing unfair.</i></li>

<li>Human Resources – <i>the HR team
will be the people who hire and fire staff, thus being an important aspect to
the game... they are also the people who get the final word on who gets banned,
how long for and how they should be banned etc.</i></li>
<li>Accountants
– <i>The accountants foresee all money transactions in the
game, they control the amount of money available in game, look over player
transactions so that if a player has a duplicate account and is transferring
money over to that account appropriate action can be taken against him/her</i></li>

<li>Newspaper Editors / Developers – <i>The newspaper staff will edit publish and develop the
MFB Times this is just a simple job but is still quite useful for the game as
it could keep people updated.</i></li></ol>

